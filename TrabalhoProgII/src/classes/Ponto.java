package classes;

/**
 *
 * @author tc claudio
 */
public class Ponto {
    protected double x;
    protected double y;
    protected int sinal;

    public int getSinal() {
        return sinal;
    }

    public void setSinal(int sinal) {
        this.sinal = sinal;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
    
}
